#include"ft_printf.h"

int	ft_putstr(char *s)
{
	int	len;

	len = 0;
	if (!s)
		return (ft_putstr("(null)"));
	if (s != 0)
	{
		while (*s)
		{
			len += ft_putchar(*s);
			s++;
		}
	}
	return (len);
}
